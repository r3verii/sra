def _diff_summary(repo_a: Path, repo_b: Path) -> str:
    """Build a markdown block describing the file-level delta between two trees.

    Output structure:

      * Added (in repo_b, not in repo_a) — up to N entries
      * Removed (in repo_a, not in repo_b) — up to N entries
      * Common (in both)                  — count + total byte delta
      * Sample of common files with the largest byte delta
    """
    a_files = _collect_repo_files(repo_a)
    b_files = _collect_repo_files(repo_b)
    a_set = set(a_files)
    b_set = set(b_files)

    added = sorted(b_set - a_set)
    removed = sorted(a_set - b_set)
    common = sorted(a_set & b_set)

    def _bullet_list(items: list[str], cap: int) -> str:
        if not items:
            return "_(none)_\n"
        capped = items[:cap]
        rest = len(items) - len(capped)
        lines = "\n".join(f"- `{p}`" for p in capped)
        if rest > 0:
            lines += f"\n- _… and {rest} more_"
        return lines + "\n"

    a_bytes_total = 0
    b_bytes_total = 0
    sample_changed: list[tuple[str, int, int]] = []
    for rel in common[:1000]:  # cap to keep this fast on huge repos
        a_path = repo_a / rel
        b_path = repo_b / rel
        try:
            a_size = a_path.stat().st_size
            b_size = b_path.stat().st_size
        except OSError:
            continue
        a_bytes_total += a_size
        b_bytes_total += b_size
        if a_size != b_size:
            sample_changed.append((rel, a_size, b_size))

    sample_changed.sort(key=lambda t: abs(t[1] - t[2]), reverse=True)
    sample_lines = (
        "\n".join(
            f"- `{rel}` ({a} -> {b} bytes, delta {b - a:+d})"
            for rel, a, b in sample_changed[:_DIFF_MAX_FILES_PER_BUCKET]
        )
        or "_(no size-changed common files detected)_"
    )

    return (
        f"## Differential summary (naive)\n"
        f"\n"
        f"This is a file-level navigation hint, not a precise diff.\n"
        f"The skill is responsible for inspecting individual files.\n"
        f"\n"
        f"- repo_a: `{repo_a}` ({len(a_files)} files)\n"
        f"- repo_b: `{repo_b}` ({len(b_files)} files)\n"
        f"- common: {len(common)} files "
        f"({a_bytes_total} -> {b_bytes_total} bytes, "
        f"delta {b_bytes_total - a_bytes_total:+d})\n"
        f"\n"
        f"### Added (only in repo_b)\n"
        f"\n"
        f"{_bullet_list(added, _DIFF_MAX_FILES_PER_BUCKET)}"
        f"\n"
        f"### Removed (only in repo_a)\n"
        f"\n"
        f"{_bullet_list(removed, _DIFF_MAX_FILES_PER_BUCKET)}"
        f"\n"
        f"### Common files with size delta\n"
        f"\n"
        f"{sample_lines}\n"
    )


def _git_diff_stat(repo_a: Path, repo_b: Path) -> str | None:
    """If both paths are inside a git worktree, return ``git diff --stat`` output.

    Best-effort and naive: runs ``git diff --stat`` inside ``repo_a``
    only when both paths have a ``.git`` entry of their own. Returns
    ``None`` if anything goes wrong (the caller falls back to
    :func:`_diff_summary`).
    """
    git = shutil.which("git")
    if not git:
        return None
    if not (repo_a / ".git").exists() or not (repo_b / ".git").exists():
        return None
    try:
        proc = subprocess.run(
            [git, "diff", "--stat", "--no-color"],
            cwd=str(repo_a),
            capture_output=True, text=True,
            encoding="utf-8", errors="replace",
            timeout=60,
        )
    except (subprocess.TimeoutExpired, OSError):
        return None
    if proc.returncode != 0:
        return None
    out = proc.stdout or ""
    if not out.strip():
        return "## git diff --stat\n\n_(empty — working tree clean on repo_a)_\n"
    return f"## git diff --stat (run inside repo_a)\n\n```\n{out}\n```\n"


