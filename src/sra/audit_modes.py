def _now_iso() -> str:
    """Return current UTC time as an ISO-8601 timestamp (no microseconds)."""
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat()


def _attribution_header(skill_name: str, mode: str, repo: Path) -> str:
    """Return a markdown header that credits the Trail of Bits skill."""
    return (
        f"# {mode} mode — {skill_name}\n"
        f"\n"
        f"_Generated by `sra audit --mode {mode}` on {_now_iso()}._\n"
        f"\n"
        f"_Target repo: `{repo}`_\n"
        f"\n"
        f"_Skill: Trail of Bits `{skill_name}` (CC-BY-SA-4.0). "
        f"See `LICENSE_THIRD_PARTY.md` for attribution. The text below "
        f"is the model's response; the skill text itself is not copied "
        f"into this report._\n"
        f"\n"
        f"---\n"
        f"\n"
    )


def _collect_repo_files(repo: Path, max_files: int = 5000) -> list[str]:
    """Walk ``repo`` and return relative POSIX file paths, deterministically ordered.

    Skips ``_SKIP_DIRECTORIES`` and stops at ``max_files`` to keep the
    diff payload bounded. The order is the OS walk order sorted, so
    successive calls on the same tree produce the same list.
    """
    files: list[str] = []
    for dirpath, dirnames, filenames in os.walk(repo):
        # in-place filter so the walker doesn't descend into skips
        dirnames[:] = sorted(d for d in dirnames if d not in _SKIP_DIRECTORIES)
        rel_dir = os.path.relpath(dirpath, repo)
        for fn in sorted(filenames):
            rel = fn if rel_dir == "." else f"{rel_dir}/{fn}"
            files.append(rel.replace(os.sep, "/"))
            if len(files) >= max_files:
                return files
    return files


def _read_existing_report(repo: Path) -> str | None:
    """Return the contents of ``<repo>/.audit/05-report/repo-report.md`` if present."""
    p = repo / ".audit" / "05-report" / "repo-report.md"
    if not p.is_file():
        return None
    try:
        return p.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return None


def _find_test_dirs(repo: Path) -> list[Path]:
    """Return absolute paths to any conventional test directories under ``repo``."""
    out: list[Path] = []
    for dirpath, dirnames, _ in os.walk(repo):
        dirnames[:] = [d for d in dirnames if d not in _SKIP_DIRECTORIES]
        for d in list(dirnames):
            if d.lower() in _TEST_DIR_NAMES:
                out.append(Path(dirpath) / d)
        # Stop descent inside test dirs themselves — we only want the
        # outermost test roots, the listing below walks them fully.
        dirnames[:] = [d for d in dirnames if d.lower() not in _TEST_DIR_NAMES]
    return sorted(out)


def _count_test_files(test_dirs: list[Path]) -> tuple[int, list[Path]]:
    """Walk each test dir; return (total_count, sample[]) of test source files.

    "Test source" is heuristic: any file under a test dir whose extension is
    one of {.py, .js, .ts, .tsx, .jsx, .go, .rs, .java, .rb, .php, .c, .cpp,
    .h, .hpp, .swift, .kt, .scala, .ex, .exs}. The sample is up to
    ``_TEST_SAMPLE_LIMIT`` files, drawn in walk order.
    """
    exts = {
        ".py", ".js", ".ts", ".tsx", ".jsx", ".go", ".rs", ".java",
        ".rb", ".php", ".c", ".cpp", ".h", ".hpp", ".swift", ".kt",
        ".scala", ".ex", ".exs",
    }
    total = 0
    sample: list[Path] = []
    for root in test_dirs:
        for dirpath, dirnames, filenames in os.walk(root):
            dirnames[:] = [d for d in dirnames if d not in _SKIP_DIRECTORIES]
            for fn in sorted(filenames):
                ext = Path(fn).suffix.lower()
                if ext in exts:
                    total += 1
                    if len(sample) < _TEST_SAMPLE_LIMIT:
                        sample.append(Path(dirpath) / fn)
    return total, sample


def _excerpt(path: Path, max_lines: int = _TEST_EXCERPT_LINES) -> str:
    """Return up to ``max_lines`` lines of ``path``, or empty on read error."""
    try:
        with path.open("r", encoding="utf-8", errors="replace") as fh:
            return "".join(line for _, line in zip(range(max_lines), fh))
    except OSError:
        return ""


