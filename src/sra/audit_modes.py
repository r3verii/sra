def _repo_structure_summary(repo: Path) -> str:
    """Tiny markdown snapshot: file count, top extensions, top-level dirs."""
    files = _collect_repo_files(repo)
    ext_counts: dict[str, int] = {}
    for f in files:
        ext = Path(f).suffix.lower()
        ext_counts[ext] = ext_counts.get(ext, 0) + 1
    top_exts = sorted(ext_counts.items(), key=lambda kv: -kv[1])[:10]
    top_dirs: list[str] = []
    try:
        for entry in sorted(repo.iterdir()):
            if entry.is_dir() and entry.name not in _SKIP_DIRECTORIES:
                top_dirs.append(entry.name)
    except OSError:
        pass
    return (
        f"## Repo structure snapshot\n"
        f"\n"
        f"- root: `{repo}`\n"
        f"- file count: {len(files)} (capped at 5000)\n"
        f"- top extensions: "
        + ", ".join(f"{ext or '<none>'}={n}" for ext, n in top_exts)
        + "\n"
        f"- top-level directories: "
        + ", ".join(f"`{d}`" for d in top_dirs[:20])
        + "\n"
    )


def _test_layout_summary(repo: Path, *, emphasis: str = "tests") -> str:
    """Markdown block describing the repo's test directory layout.

    ``emphasis`` is woven into the prose so the skill downstream knows
    which kind of test artefact the mode cares about (``mutation`` vs
    ``property-based``).
    """
    test_dirs = _find_test_dirs(repo)
    if not test_dirs:
        return (
            f"## Test layout ({emphasis})\n"
            f"\n"
            f"_No conventional test directory found_ "
            f"(searched basenames: {sorted(_TEST_DIR_NAMES)}).\n"
            f"The skill should report this as a coverage gap.\n"
        )
    total, sample = _count_test_files(test_dirs)
    rel_dirs = [str(d.relative_to(repo)).replace(os.sep, "/") for d in test_dirs]
    excerpts: list[str] = []
    for s in sample:
        rel = str(s.relative_to(repo)).replace(os.sep, "/")
        text = _excerpt(s)
        if not text:
            continue
        excerpts.append(
            f"#### `{rel}` (first {_TEST_EXCERPT_LINES} lines)\n\n"
            f"```\n{text.rstrip()}\n```\n"
        )
    excerpts_md = "\n".join(excerpts) if excerpts else "_(no excerpts available)_\n"
    return (
        f"## Test layout ({emphasis})\n"
        f"\n"
        f"- test directories ({len(test_dirs)}): "
        + ", ".join(f"`{d}`" for d in rel_dirs)
        + "\n"
        f"- total test files (heuristic, by extension): {total}\n"
        f"- sampling up to {_TEST_SAMPLE_LIMIT} files for excerpts below\n"
        f"\n"
        f"### Sample test excerpts\n"
        f"\n"
        f"{excerpts_md}"
    )


