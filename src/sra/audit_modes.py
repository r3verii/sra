def _invoke_claude(
    prompt: str, *, repo_cwd: Path, model: str | None,
    timeout: int = 3600,
) -> tuple[int, str, str]:
    """Pipe ``prompt`` into ``claude -p``, return (rc, stdout, stderr).

    Mirrors the nested-session guard used elsewhere in the project
    (clears ``CLAUDECODE`` / ``CLAUDE_CODE_ENTRYPOINT``). Uses
    ``repo_cwd`` as the working directory so Read/Grep tools resolve
    against the target.

    Routes via ``cli._run_claude_with_heartbeat`` so the live child is
    tracked in the project-wide ``_LIVE_PROCS`` registry and the user
    can stop the run with Ctrl+C. Default timeout raised to 3600s
    (1 h) — alt-modes run a single whole-repo pass and 20 min was
    routinely too short.
    """
    claude = shutil.which("claude")
    if not claude:
        return 127, "", "`claude` CLI not found on PATH"

    env = os.environ.copy()
    env.pop("CLAUDECODE", None)
    env.pop("CLAUDE_CODE_ENTRYPOINT", None)

    cmd = [claude, "-p"]
    if model:
        cmd.extend(["--model", model])

    # Import lazily to avoid circular import (cli.py imports this module).
    from sra.cli import _run_claude_with_heartbeat
    try:
        proc = _run_claude_with_heartbeat(
            cmd, input_text=prompt, timeout=timeout,
            cwd=str(repo_cwd), env=env,
            label=f"audit-mode [{model}]" if model else "audit-mode",
        )
    except subprocess.TimeoutExpired:
        return 124, "", f"claude -p timed out after {timeout} s"
    except KeyboardInterrupt:
        return 130, "", "interrupted by user"

    return proc.returncode, proc.stdout or "", proc.stderr or ""


def _write_output(out_path: Path, header: str, body: str, stderr: str) -> int:
    """Write ``header`` + ``body`` (+ stderr if present) to ``out_path``.

    Atomic write (temp file + os.replace) so an interrupt mid-write
    leaves the previous output (or nothing) intact rather than a
    truncated artefact masquerading as a finished report.

    Returns 0 on success, 1 on write failure. Parent dirs are created.
    """
    try:
        out_path.parent.mkdir(parents=True, exist_ok=True)
        combined = header + body
        if stderr:
            combined += (
                f"\n\n---\n\n## stderr from claude -p\n\n"
                f"```\n{stderr.rstrip()}\n```\n"
            )
        tmp = out_path.with_suffix(out_path.suffix + ".tmp")
        tmp.write_text(combined, encoding="utf-8")
        os.replace(tmp, out_path)
    except OSError as e:
        print(f"error: cannot write {out_path}: {e}", file=sys.stderr)
        return 1
    return 0


def _ensure_dir(repo_str: str) -> Path | None:
    """Resolve and validate a repo path. Returns ``None`` after printing on error."""
    p = Path(repo_str).expanduser()
    if not p.is_dir():
        print(f"error: not a directory: {p}", file=sys.stderr)
        return None
    return p.resolve()


