"""sra audit alternative modes — whole-repo, single-invocation flows.

Each mode in this module is a complement to the default ``sra audit``
pipeline. The default audit composes deterministic sensor + packet
stages and per-packet skill invocations. The modes here skip that
entirely: they load **one** Trail of Bits SKILL.md, assemble a
mode-specific extra-context block (a diff summary, a spec document,
a test-layout snapshot), pipe the bundle into ``claude -p``, and
write the result under ``<repo>/.audit/05-report/``.

The four modes (registered in :mod:`sra.skill_registry` with
``stage="alt-mode"`` / ``trigger="per-mode"``):

* ``differential``           -> ``differential-review`` skill
* ``spec-to-code``           -> ``spec-to-code-compliance`` skill
* ``mutation-testing``       -> ``mutation-testing`` skill
* ``property-based-testing`` -> ``property-based-testing`` skill

These are dispatched from ``sra.cli.main`` when ``--mode`` is set to
anything other than ``audit``. The default ``audit`` mode continues
to call ``sra.cli.cmd_audit`` untouched.

Design notes (Phase 6):

* These modes don't have packet builders or families - they're
  whole-repo single-invocation flows. The skill is responsible for
  the comparison logic; we only feed it a small structured hint.
* The differential summary is intentionally naive: file-level
  added / removed / common, plus a per-file byte-size delta. It's a
  navigation hint for the skill, not a precise diff. If both
  repos share a git history, ``--diff-tool git`` swaps in
  ``git diff --stat`` output instead.
* The spec read is whole-file, capped at 50 KB (the skill is
  responsible for chunking it further if needed).
* For mutation / property modes, we surface the repo's test
  directory layout, a count of test files, and short excerpts
  from a few of them so the skill has something concrete to
  reason about.
* ``--no-skills`` is rejected by the CLI dispatcher with a clear
  error: the alt-mode skills *are* the entire mode. Skipping
  them means doing nothing, which is more useful as an explicit
  failure than a silent no-op.
"""

from __future__ import annotations

import os
import shutil
import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path

from sra.skill_loader import load_skill_prompt


# Directories the file walker never descends into. Mirrors
# ``sra.cli.SKIP_DIRECTORIES`` — kept in sync by hand because importing
# from cli.py would create a cycle (cli imports this module).
_SKIP_DIRECTORIES: set[str] = {
    ".git", ".hg", ".svn",
    "node_modules", "bower_components", "vendor",
    "__pycache__", ".venv", "venv", "env",
    ".tox", ".nox",
    ".mypy_cache", ".pytest_cache", ".ruff_cache",
    "dist", "build", "target", "out",
    ".idea", ".vscode", ".vs",
    ".gradle", ".next", ".nuxt", ".svelte-kit",
    ".audit",
}


# Conservative cap for the spec file fed into spec-to-code-compliance.
# The skill is responsible for further chunking; we just guarantee a
# single invocation does not blow up the prompt.
_SPEC_MAX_BYTES = 50 * 1024


# Cap how many files we list in the diff summary buckets. The skill
# treats this as a navigation hint, not a precise inventory.
_DIFF_MAX_FILES_PER_BUCKET = 200


# Cap how many test files we excerpt for mutation / property modes.
_TEST_SAMPLE_LIMIT = 5
_TEST_EXCERPT_LINES = 40


# Conventional test-directory basenames (lowercased) the walkers look
# for. Matches the role group in ``sra.cli.DIRECTORY_ROLE_NAMES``.
_TEST_DIR_NAMES: set[str] = {
    "tests", "test", "spec", "specs", "regression", "__tests__",
}


